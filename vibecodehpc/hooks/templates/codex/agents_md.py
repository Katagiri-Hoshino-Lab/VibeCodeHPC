def generate_anti_idle_directive(
    role: str,
    agent_id: str = "",
    max_idle_sec: int = 60,
) -> str:
    """Generate the anti-idle section for AGENTS.md.

    Args:
        role: Agent role (PM, SE, PG, CD, SOLO).
        agent_id: Agent identifier (e.g. "PG1.1").
        max_idle_sec: Maximum allowed idle time before the external
            idle detector intervenes.

    Returns:
        Markdown string for the anti-idle directive section.
    """
    role_info = _ROLE_TASKS.get(role, _ROLE_TASKS["PG"])
    tasks = role_info["polling_tasks"]
    check_files = role_info["check_files"]

    task_list = "\n".join(f"{i}. {t}" for i, t in enumerate(tasks, 1))
    file_list = "\n".join(f"- `{f}`" for f in check_files)

    return f"""\
## Anti-Idle Directive (CRITICAL)

**You are a polling agent. You must NEVER idle or stop.**

You are a polling agent ({agent_id or role}). You MUST NOT stop, wait, or
idle under any circumstances. If you have no pending tasks, you MUST
actively search for work by checking the files and tasks listed below.

An external idle detector monitors your activity. If no tool calls are
detected for {max_idle_sec} seconds, you will be forcibly restarted.

### Polling Tasks (by priority)
{task_list}

### Periodic Check Files
{file_list}

### Rules
- Immediately search for the next task after completing one
- Declarations like "done" or "waiting" are prohibited
- When unsure, re-read ChangeLog.md and ToDoList.md
- Communicate with other agents via agent_send.sh
- Repeat steps 1-7 in an infinite loop
"""


def generate_agents_md(
    role: str,
    agent_id: str = "",
    project_name: str = "VibeCodeHPC",
    extra_instructions: str = "",
    max_idle_sec: int = 60,
) -> str:
    """Generate complete AGENTS.md content for a Codex agent.

    Args:
        role: Agent role (PM, SE, PG, CD, SOLO).
        agent_id: Agent identifier.
        project_name: Project name for the header.
        extra_instructions: Additional markdown to append.
        max_idle_sec: Idle timeout for anti-idle directive.

    Returns:
        Complete AGENTS.md content string.
    """
    role_info = _ROLE_TASKS.get(role, _ROLE_TASKS["PG"])

    header = f"""\
# {project_name} — {role} Agent ({agent_id or role})

{role_info["description"]}

"""

    anti_idle = generate_anti_idle_directive(role, agent_id, max_idle_sec)

    communication = f"""\
## Communication

Use `agent_send.sh` for inter-agent communication:
```bash
agent_send.sh <recipient> "[{agent_id or role}] message content"
```

- To PM: `agent_send.sh PM "[{agent_id or role}] report content"`
- To SE: `agent_send.sh SE1 "[{agent_id or role}] request content"`
- Broadcast: `agent_send.sh ALL "[{agent_id or role}] shared information"`
"""

    override_note = """\
## Override

If `AGENTS.override.md` exists, its contents will be appended to this file.
Write project-specific settings in the override file.
"""

    parts = [header, anti_idle, communication, override_note]
    if extra_instructions:
        parts.append(f"\n## Additional Instructions\n\n{extra_instructions}\n")

    return "\n".join(parts)


def write_agents_md(
    target_dir,
    role: str,
    agent_id: str = "",
    **kwargs,
):
    """Write AGENTS.md to a directory.

    Args:
        target_dir: Directory where AGENTS.md will be written.
        role: Agent role.
        agent_id: Agent identifier.
        **kwargs: Forwarded to :func:`generate_agents_md`.

    Returns:
        Path to the written file.
    """
    from pathlib import Path

    target_dir = Path(target_dir)
    target_dir.mkdir(parents=True, exist_ok=True)
    out_path = target_dir / "AGENTS.md"
    content = generate_agents_md(role=role, agent_id=agent_id, **kwargs)
    out_path.write_text(content, encoding="utf-8")
    return out_path
