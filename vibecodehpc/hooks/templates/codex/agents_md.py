#!/usr/bin/env python3
"""Generate AGENTS.md anti-idle directives for Codex CLI.

Codex CLI reads ``AGENTS.md`` (and ``AGENTS.override.md``) as its
instruction file.  Since Codex has no runtime hooks, polling behavior
must be encoded as strong instruction-level directives.

This module generates role-specific AGENTS.md content that keeps
Codex agents working continuously, mirroring the anti-idle guarantees
that Claude/Gemini achieve via Stop hook exit-code-2 blocking.

Usage:
    from vibecodehpc.hooks.templates.codex.agents_md import generate_agents_md

    content = generate_agents_md(role="PG", agent_id="PG1.1")
    Path("AGENTS.md").write_text(content)
"""

